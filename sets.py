# creating a set
programming_set = {"python", "c++", "java", "ruby"}
print("Original set:", programming_set)

# adding operation in set
programming_set.add("kotlin")
print("set after adding an element:", programming_set)

# removing operation in set
programming_set.remove("java")
print("set after an element:", programming_set)

# modifying operation in set
programming_set.remove("kotlin")
programming_set.add("MySQL")
print("set after modifying an element:", programming_set)
