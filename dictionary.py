# Creating a dictionary
students_dict = {"William": 12,"Tia": 15,"David": 19,"Harris": 18}
print("Dictionary:", students_dict)

# addding an element key val
students_dict["John"] = 17
print("Dictionary after adding element:", students_dict)

# removing an element key val
del students_dict["Tia"]
print("Dictionary after removing element:", students_dict)


# modifying an element key val
students_dict['William'] = 21
print("Dictionary after modifying element:", students_dict)