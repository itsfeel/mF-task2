# creating a list
shopping_List = ["eggs", "milk", "butter", "avocado", "tofu"]
print("Original list:", shopping_List)

# adding an element
shopping_List.append("soyachunk")
print("List after adding an element:", shopping_List)

# removing an element
shopping_List.remove("tofu")
print("List after removing an element:", shopping_List)

# modifying an element
shopping_List[1] = "Jam"
print("List after modifying an element:", shopping_List)